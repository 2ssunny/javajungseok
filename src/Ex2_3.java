public class Ex2_3 {

    public static void main(String[] args) {
        System.out.println(5+3); //화면에 5+3외 결과인 8이 출력된다
        System.out.println(5-3); //화면에 5-3외 결과인 2이 출력된다
        System.out.println(5*3); //화면에 5*3외 결과인 15이 출력된다
        System.out.println(5/3); //화면에 5/3외 결과인 1이 출력된다
    }
}